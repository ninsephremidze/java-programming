package day39_Wrapper;

public class autoboxing {
    public static void main(String[] args) {
        //autoboxing
        int num1=500;
        Integer num2=num1;
        Integer num3=234;


        //unboxing
        Double d1=new Double(100.5);
        double d2=d1;
        double d3=new Double(345.3);

    }
}
